package Pattern;

public class pattern20 {
public static void main(String[] args) {
        int a=5;
        int b=7;
        for(int i=1;i<=a;i++){
            for(int j=0;j<i;j++){
                System.out.print("*");
            }for(int k=0;k<b;k++){
                System.out.print(" ");
            }while(i<a){for(int l=0;l<i;l++){
                System.out.print("*");
            }}
        
        System.out.println();
        b-=2;}
    }}
